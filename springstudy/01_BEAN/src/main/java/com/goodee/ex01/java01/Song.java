package com.goodee.ex01.java01;

public class Song {

	// field
	private String title;  // 제목
	private String genre;  // 장르
	
	// getter/setter
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	
}
